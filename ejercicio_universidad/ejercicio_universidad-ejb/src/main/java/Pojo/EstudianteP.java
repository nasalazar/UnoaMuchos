public class EstudianteP {
    private int id;
    private String nombre;
    private int cedula;
    private int id_dip;

    public EstudianteP() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getCedula() {
        return cedula;
    }

    public void setCedula(int cedula) {
        this.cedula = cedula;
    }

    public int getId_dip() {
        return id_dip;
    }

    public void setId_dip(int id_dip) {
        this.id_dip = id_dip;
    }
    
    
    
}
